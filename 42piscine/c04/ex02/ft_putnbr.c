#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	i;

	if (nb < 0)
	{
		if (nb == -2147483648)
			write(1, "-2147483648", 11);
		else
		{
			nb *= -1;
			write(1, "-", 1);
		}
	}
	if (nb >= 0)
	{
		i = nb % 10 + '0';
		if (nb > 9)
		{
			nb = nb / 10;
			ft_putnbr(nb);
		}
		write(1, &i, 1);
	}
}
