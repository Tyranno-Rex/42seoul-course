int	ft_str_is_alpha(char *str)
{
	int		j;

	j = 0;
	while (str[j] != '\0')
	{	
		if ((str[j] < 'A') || (str[j] > 'Z' && str[j] < 'a') || (str[j] > 'z'))
			return (0);
		j++;
	}
	if (j == 0)
		return (1);
	return (1);
}
