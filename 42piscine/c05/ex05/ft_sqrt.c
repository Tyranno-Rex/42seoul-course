int	ft_sqrt(int nb)
{
	int		i;

	i = 3;
	if (nb < 1)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	while (i < nb)
	{
		if (i * i == nb)
			return (i);
		else if (i * i > nb)
			break ;
		i++;
	}
	return (0);
}
