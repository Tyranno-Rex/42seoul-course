int	ft_is_prime(int nb)
{
	int		i;

	i = 2;
	if (nb == 2)
		return (1);
	else if (nb <= 1 || nb % 2 == 0)
		return (0);
	while (i < nb && i < 46341)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
