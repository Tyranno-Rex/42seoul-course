char	*ft_strcat(char *dest, char *src)
{
	int		i[2];

	i[0] = 0;
	while (dest[i[0]] != '\0')
		i[0]++;
	i[1] = 0;
	while (src[i[1]] != '\0')
	{
		dest[i[0]] = src[i[1]];
		i[0]++;
		i[1]++;
	}
	dest[i[0]] = '\0';
	return (dest);
}
