char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned int	i[2];

	i[0] = 0;
	if (nb < 0)
		return (0);
	while (dest[i[0]] != '\0')
		i[0]++;
	i[1] = 0;
	while (i[1] < nb && src[i[1]] != '\0')
	{
		dest[i[0]] = src[i[1]];
		i[0]++;
		i[1]++;
	}
	dest[i[0]] = '\0';
	return (dest);
}
